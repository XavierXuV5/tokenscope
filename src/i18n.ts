export type Messages = (typeof dictionaries)[Locale];

export function normalizeLocale(value?: string | null): Locale | null {
  if (!value) return null;
  const lower = value.toLowerCase();
  if (lower === "ja" || lower.startsWith("ja-")) return "ja";
  if (lower === "zh-tw" || lower === "zh-hk" || lower === "zh-mo" || lower.includes("hant")) return "zh-TW";
  if (lower === "zh-cn" || lower === "zh-sg" || lower.includes("hans") || lower === "zh" || lower.startsWith("zh-")) return "zh-CN";
  if (lower === "en" || lower.startsWith("en-")) return "en";
  return null;
}

export function detectLocale(): Locale {
  if (typeof navigator === "undefined") return "en";
  for (const lang of navigator.languages || [navigator.language]) {
    const locale = normalizeLocale(lang);
    if (locale) return locale;
  }
  return "en";
}

export function getMessages(locale: Locale): Messages {
  return dictionaries[locale] || dictionaries.en;
}

export function localeToIntl(locale: Locale) {
  return locale === "en" ? "en-US" : locale;
}
